public class Nuoc {
    String tenNuoc;
    int icon;

    public Nuoc(String tenNuoc, int icon) {
        this.tenNuoc = tenNuoc;
        this.icon = icon;
    }

    public String getTenNuoc() {
        return tenNuoc;
    }

    public void setTenNuoc(String tenNuoc) {
        this.tenNuoc = tenNuoc;
    }

    public int getIcon() {
        return icon;
    }

    public void setIcon(int icon) {
        this.icon = icon;
    }
}
